# Nädal 3 – SQL JOINs

**Autor:** Liis Kolga

## Kontekst

Minu ülesanne oli analüüsida UrbanStyle müügikanaleid SQL JOIN päringute abil. Kasutasin tabelite `sales`, `customers` ja `products` andmeid, et ühendada erinevates tabelites olev info ning võrrelda müügikanalite tulemuslikkust.

## Minu töö

Koostasin SQL JOIN päringud, millega:

- tuvastasin kasutusel olevad müügikanalid;
- arvutasin klientide arvu müügikanalite lõikes;
- arvutasin kogumüügi mõlemas müügikanalis;
- leidsin keskmise müügi ühe kliendi kohta;
- võrdlesin saadud tulemusi müügikanalite vahel.

## Tulemused

Analüüs näitas, et UrbanStyle kasutab kahte müügikanalit: **pood** ja **online**.

Füüsilises poes ostles **2278 klienti**, kogumüük oli **1 902 430 €** ning keskmine müük ühe kliendi kohta **835.13 €**.

Veebikanalis ostles **1706 klienti**, kogumüük oli **1 006 747 €** ning keskmine müük ühe kliendi kohta **590.12 €**.

Tulemused näitavad, et füüsiline pood teenindas rohkem kliente, teenis suurema kogumüügi ning ühe kliendi kohta teeniti rohkem tulu kui veebikanalis.

## Järeldus

SQL JOIN päringute abil ühendati erinevates tabelites olevad andmed ning võrreldi UrbanStyle müügikanalite tulemuslikkust. Analüüsi põhjal oli füüsiline pood edukam nii klientide arvu, kogumüügi kui ka keskmise müügi poolest ühe kliendi kohta.

